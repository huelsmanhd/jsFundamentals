// Comparison Operators

// Equal
3 == 3
'3' == 3

// Unequal
3 != 4
3 != '4'

// Strict Equal
3 === 3
'3' === 3 //false

// Strict Not Equal
3 !== 4
'3' !== 3
'3' !== 4 //false

// Greater Than
3 > 2

// Greater Than or Equal
5 >= 4
4 >= 4
3 >= 4 //false

// Less Than
6 < 8

// Less Than or Equal
4 <= 4
4 <= 5

// AND
true && true
2 === 2 && 6 !== 7
true && false //false

// OR
false || true
7 === 7 || 4 >= 5
false || false //false

